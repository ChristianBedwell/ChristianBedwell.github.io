
public class Links {
	private String self;
	private String alternate;
	private String cast;
	private String clips;
	private String reviews;
	private String similar;
	private String next;
	
	// getter and setter for "self"
	public String getSelf() {
		return self;
	}
	
	public void setSelf(String self) {
		this.self = self;
	}
	
	// getter and setter for "alternate"
	public String getAlternate() {
		return alternate;
	}
	
	public void setAlternate(String alternate) {
		this.alternate = alternate;
	}
	
	// getter and setter for "cast"
	public String getCast() {
		return cast;
	}
	
	public void setCast(String cast) {
		this.cast = cast;
	}
	
	// getter and setter for "clips"
	public String getClips() {
		return clips;
	}
	
	public void setClips(String clips) {
		this.clips = clips;
	}
	
	// getter and setter for "reviews"
	public String getReviews() {
		return reviews;
	}
	
	public void setReviews(String reviews) {
		this.reviews = reviews;
	}
	
	// getter and setter for "similar"
	public String getSimilar() {
		return similar;
	}
	
	public void setSimilar(String similar) {
		this.similar = similar;
	}
	
	// getter and setter for "next"
	public String getNext() {
		return next;
	}
	
	public void setNext(String next) {
		this.next = next;
	}
}
